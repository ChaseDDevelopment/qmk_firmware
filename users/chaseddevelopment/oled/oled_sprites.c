void chased_render_luna(uint8_t x, uint8_t y) {
    bool sneaking = chased_pet_is_sneaking();
    bool jumping  = chased_pet_is_jumping();
    uint8_t wpm   = (uint8_t)get_current_wpm();
    led_t led     = host_keyboard_led_state();

    // Clear footprints depending on jump state
    if (jumping || !showedJump) {
        oled_set_cursor(x, y + 2);
        oled_write("     ", false);
        if (y > 0) {
            oled_set_cursor(x, y - 1);
        }
        showedJump = true;
    } else {
        if (y > 0) {
            oled_set_cursor(x, y - 1);
            oled_write("     ", false);
        }
        oled_set_cursor(x, y);
    }

    // Choose frame based on state
    const char* frame = NULL;
    if (led.caps_lock) {
        frame = bark[current_frame];
    } else if (sneaking) {
        frame = sneak[current_frame];
    } else if (wpm <= 10) {
        frame = sit[current_frame];
    } else if (wpm <= 40) {
        frame = walk[current_frame];
    } else {
        frame = run[current_frame];
    }
    oled_write_raw_P(frame, ANIM_SIZE);
}

void chased_update_luna_anim(void) {
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        anim_timer    = timer_read32();
        current_frame = (current_frame + 1) % 2;
    }
}

static uint8_t wpm_hist[32] = {0};
static uint8_t wpm_hist_idx = 0;

static uint8_t scale_wpm_to_8(uint8_t wpm) {
    uint16_t v = wpm;
    if (v > 128) v = 128;
    uint8_t h = (uint8_t)((v + 8) / 16);
    if (h > 8) h = 8;
    return h;
}

void chased_render_wpm_bar_line(uint8_t line, uint8_t start_col, uint8_t width) {
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        wpm_hist_idx = (wpm_hist_idx - 1) & 31;
        wpm_hist[wpm_hist_idx] = scale_wpm_to_8((uint8_t)get_current_wpm());
    }

    uint8_t buf[64];
    if (width > sizeof(buf)) width = sizeof(buf);
    for (uint8_t i = 0; i < width; i++) {
        uint8_t sample = wpm_hist[(wpm_hist_idx + i) & 31];
        uint8_t h8     = (sample > 8) ? 8 : sample;
        buf[i]         = (h8 == 0) ? 0x00 : (uint8_t)((1U << h8) - 1U);
    }
    oled_set_cursor(start_col, line);
    oled_write_raw((const char *)buf, width);
}

void chased_render_caps_dot_if_any(uint8_t line) {
    led_t st = host_keyboard_led_state();
    if (st.caps_lock) {
        oled_set_cursor(20, line);
        oled_write_P(PSTR("."), false);
    }
}

