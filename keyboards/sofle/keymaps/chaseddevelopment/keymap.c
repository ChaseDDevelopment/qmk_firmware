#ifdef RGB_MATRIX_ENABLE
// Gaming overlay: keep base muted and highlight WASD as a static accent
bool rgb_matrix_indicators_advanced_user(uint8_t led_min, uint8_t led_max) {
    if (get_highest_layer(layer_state) == _GAMING) {
        // Sofle WASD positions (shifted down 1 row from Corne):
        // W: row 1, col 2
        // A: row 2, col 1
        // S: row 2, col 2
        // D: row 2, col 3
        const uint8_t coords[][2] = {
            {1, 2}, // W
            {2, 1}, // A
            {2, 2}, // S
            {2, 3}  // D
        };
        for (uint8_t i = 0; i < sizeof(coords) / sizeof(coords[0]); i++) {
            uint8_t row = coords[i][0];
            uint8_t col = coords[i][1];
            uint8_t idx = g_led_config.matrix_co[row][col];
            if (idx != NO_LED && idx >= led_min && idx < led_max) {
                rgb_matrix_set_color(idx, 0, 150, 150); // Darker teal
            }
        }
    }
    return false;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    static uint8_t s_last_layer = _QWERTY;
    uint8_t current = get_highest_layer(state);
    // When leaving GAMING, clear any active modifiers more safely
    if (s_last_layer == _GAMING && current != _GAMING) {
        // Clear any held keys first to prevent ghost inputs
        clear_keyboard();
        // Small delay for USB processing
        wait_ms(10);
        // Clear modifier states
        clear_mods();
        clear_oneshot_mods();
        clear_weak_mods();
        // Final report to ensure clean state
        send_keyboard_report();
    }
    s_last_layer = current;

    switch (current) {
        case _LOWER: {
            // Lower: dark cyan/teal to combat yellow case
            rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv_noeeprom(128, 255, 60);
        } break;
        case _RAISE: {
            // Raise: dark purple
            rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv_noeeprom(191, 255, 100);
        } break;
        case _GAMING: {
            // Gaming: dark forest green to combat yellow case; WASD painted darker teal in indicators
            rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
            rgb_matrix_sethsv_noeeprom(85, 255, 50);
        } break;
        default: {
            // Base: rainbow pinwheels, slightly dimmed, slower speed
            rgb_matrix_mode_noeeprom(RGB_MATRIX_RAINBOW_PINWHEELS);
            hsv_t hsv = rgb_matrix_get_hsv();
            rgb_matrix_sethsv_noeeprom(hsv.h, 255, 80);
            rgb_matrix_set_speed_noeeprom(120);
        } break;
    }
    return state;
}
#endif

